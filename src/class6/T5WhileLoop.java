package class6;

public class T5WhileLoop {
    public static void main(String[] args) {
/*
        Print 5 10 15 20 25 30
 */
        int i= 5; //this is the start point

        while(i<=30){ //this is the end point

            System.out.println(i);
            i+=5; //amount of steps or the difference between the numbers
        }






    }
}
