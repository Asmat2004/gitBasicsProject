package review1;

public class E2Variable {
    public static void main(String[] args) {
        int age=10; //we are creating a variable and assigning a value to it
        age=12;  //we are changing the value of our previous variable
        System.out.println(age);

        int price; //you don't need to assign a value to a variable
        price=1000;
        System.out.println(price);





    }
}
